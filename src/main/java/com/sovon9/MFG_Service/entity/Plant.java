package com.sovon9.MFG_Service.entity;

import jakarta.persistence.*;

@Entity
@Table
public class Plant {

    @Id
    private String id;

    @Column
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
